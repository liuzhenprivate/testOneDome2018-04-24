package com.sinontech.entity.read;


/**
 * 页面配置模块详情表
 * @author ljj
 *
 */
public class HtmlModleDetail {
	
	private long id;

	private long htmlmodleId;
	
	private Article article;
	
	//主标题
	private String title;
	//副标题
	private String subhead;
	//内容
	private String content;
	
	//图片路径
	private String imgUrl;
		
	//图片路径
	private String htmlUrl;
	
	//模板类型1系统页面 2其他
	private int htmlType;

	//排序
	private int sort;
	//书籍ID
	private long articleID;

	//描述说明
	private String memo;
	
	// 创建时间
	private String createTime;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
  
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSubhead() {
		return subhead;
	}

	public void setSubhead(String subhead) {
		this.subhead = subhead;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public String getHtmlUrl() {
		return htmlUrl;
	}

	public void setHtmlUrl(String htmlUrl) {
		this.htmlUrl = htmlUrl;
	}

	public int getHtmlType() {
		return htmlType;
	}

	public void setHtmlType(int htmlType) {
		this.htmlType = htmlType;
	}

	public int getSort() {
		return sort;
	}

	public void setSort(int sort) {
		this.sort = sort;
	}

	public Article getArticle() {
		return article;
	}

	public void setArticle(Article article) {
		this.article = article;
	}

	public long getArticleID() {
		return articleID;
	}

	public void setArticleID(long articleID) {
		this.articleID = articleID;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public String getCreateTime() {
		return createTime;
	}

	public long getHtmlmodleId() {
		return htmlmodleId;
	}

	public void setHtmlmodleId(long htmlmodleId) {
		this.htmlmodleId = htmlmodleId;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public HtmlModleDetail() {
		super();
	}

	@Override
	public String toString() {
		return "HtmlModleDetail [id=" + id + ", htmlmodleId=" + htmlmodleId
				+ ", article=" + article + ", title=" + title + ", subhead="
				+ subhead + ", content=" + content + ", imgUrl=" + imgUrl
				+ ", htmlUrl=" + htmlUrl + ", htmlType=" + htmlType + ", sort="
				+ sort + ", articleID=" + articleID + ", memo=" + memo
				+ ", createTime=" + createTime + "]";
	}
}
